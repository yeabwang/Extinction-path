def koch_curve(length,level):
    pikachu.color('purple','violet')
    if level == 0:
        pikachu.fd(length)
    else:
        koch_curve(length/3,level-1)
        pikachu.lt(60)
        koch_curve(length/3,level-1)
        pikachu.rt(120)
        koch_curve(length/3,level-1)
        pikachu.lt(60)
        koch_curve(length/3,level-1)
